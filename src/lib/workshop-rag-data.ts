// Helper function to find workshop by name
export const findWorkshopByName = (name: string) => {
  const searchName = name.toLowerCase();
  return workshopRAGData.workshops.find(workshop => 
    workshop.name.toLowerCase().includes(searchName) ||
    workshop.fullName.toLowerCase().includes(searchName) ||
    workshop.subtitle.toLowerCase().includes(searchName)
  );
};

// Helper function to get workshop registration response
export const getWorkshopRegistrationResponse = (workshopName: string) => {
  const workshop = findWorkshopByName(workshopName);
  if (workshop) {
    return workshop.chatbotResponses.registration;
  }
  return "I'd be happy to help you register for a workshop! We offer 6 different workshops covering various aspects of women's health. Could you please specify which workshop you're interested in?";
}; 